function getTodayUTCString() {
  const now = new Date();
  const year = now.getUTCFullYear();
  const month = String(now.getUTCMonth() + 1).padStart(2, "0");
  const date = String(now.getUTCDate()).padStart(2, "0");

  return `${year}-${month}-${date}`;
}

export { getTodayUTCString };
